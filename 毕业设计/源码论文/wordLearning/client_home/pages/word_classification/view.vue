<template>
  <mm_warp>
    <mm_container class="container diy_view">
      <mm_row>
        <mm_col>
          <mm_view class="">
            <uni-forms :modelValue="form"  v-if="is_view()">
              <uni-forms-item v-if="$check_field('get','word_category') || $check_field('add','word_category') || $check_field('set','word_category')" label="单词类别" name="word_category">
                <uni-easyinput type="text" v-model="form['word_category']" v-if="user_group === '管理员' || (form['word_classification_id'] && $check_field('set','word_category')) || (!form['word_classification_id'] && $check_field('add','word_category'))" :disabled="disabledObj['word_category_isDisabled']" />
                <!-- 仅查看 -->
                <text v-else-if="$check_field('get','word_category')">
                  {{ form['word_category'] }}
                </text>
              </uni-forms-item>

            </uni-forms>
            <view class="form_button">
              <button size="mini" type="primary" @click="submit()">提交</button>
              <button size="mini" @click="cancel()">取消</button>
            </view>
          </mm_view>
        </mm_col>
      </mm_row>
    </mm_container>
  </mm_warp>
